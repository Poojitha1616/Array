public class Array {
    public static void main(String[] args) {
        int size = 5;
        int[] array = {10, 20, 30, 40, 50};

        System.out.println("Elements of the array:");
        for (int i = 0; i < size; i++) {
            System.out.print(array[i] + " ");
        }
    }
}

